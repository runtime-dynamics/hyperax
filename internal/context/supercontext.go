// writeProjectOverview writes the top-level project overview section with
// workspace name and root path.
func (g *SuperContextGenerator) writeProjectOverview(sb *strings.Builder, workspaceID, rootPath string) {
	sb.WriteString("# ")
	sb.WriteString(workspaceID)
	sb.WriteString(" — CASAT Managed Context\n\n")
	sb.WriteString("## Project Overview\n\n")
	sb.WriteString("**Workspace:** ")
	sb.WriteString(workspaceID)
	sb.WriteString("\n")
	sb.WriteString("**Root:** `")
	sb.WriteString(rootPath)
	sb.WriteString("`\n\n")
}

// writeProjectPlans queries the Projects repo for all plans in the workspace
// and writes them as a bulleted list with status and priority.
func (g *SuperContextGenerator) writeProjectPlans(ctx context.Context, sb *strings.Builder, workspaceID string) error {
	if g.store.Projects == nil {
		sb.WriteString("## Active Project Plans\n\n")
		sb.WriteString("No project management available.\n\n")
		return nil
	}

	plans, err := g.store.Projects.ListProjectPlans(ctx, workspaceID)
	if err != nil {
		sb.WriteString("## Active Project Plans\n\n")
		sb.WriteString("Unable to retrieve project plans.\n\n")
		return fmt.Errorf("context.SuperContextGenerator.writeProjectPlans: %w", err)
	}

	sb.WriteString("## Active Project Plans\n\n")

	if len(plans) == 0 {
		sb.WriteString("No project plans configured.\n\n")
		return nil
	}

	sb.WriteString("Use `get_project_details` (project_id) or `list_tasks` to view and manage:\n\n")
	for _, p := range plans {
		fmt.Fprintf(sb, "- **%s** (`%s`) — %s / %s\n", p.Name, p.ID, p.Status, p.Priority)
	}
	sb.WriteString("\n")

	return nil
}

// writeActiveTasks queries project plans and their milestones to find tasks
// with status "in-progress" and lists them prominently so agents know what
// is currently being worked on.
func (g *SuperContextGenerator) writeActiveTasks(ctx context.Context, sb *strings.Builder, workspaceID string) error {
	if g.store.Projects == nil {
		return nil
	}

	plans, err := g.store.Projects.ListProjectPlans(ctx, workspaceID)
	if err != nil {
		return fmt.Errorf("context.SuperContextGenerator.writeActiveTasks: list plans: %w", err)
	}

	var activeTasks []*taskWithContext
	for _, plan := range plans {
		milestones, err := g.store.Projects.ListMilestones(ctx, plan.ID)
		if err != nil {
			g.logger.Warn("failed to list milestones", "plan", plan.ID, "error", err)
			continue
		}
		for _, ms := range milestones {
			tasks, err := g.store.Projects.ListTasks(ctx, ms.ID)
			if err != nil {
				g.logger.Warn("failed to list tasks", "milestone", ms.ID, "error", err)
				continue
			}
			for _, t := range tasks {
				if t.Status == "in-progress" || t.Status == "in_progress" {
					activeTasks = append(activeTasks, &taskWithContext{
						Task:          t,
						PlanName:      plan.Name,
						MilestoneName: ms.Name,
					})
				}
			}
		}
	}

	if len(activeTasks) == 0 {
		return nil
	}

	sb.WriteString("## Active Tasks\n\n")
	sb.WriteString("Tasks currently in progress:\n\n")
	for _, at := range activeTasks {
		fmt.Fprintf(sb, "- **%s** (`%s`) — %s / %s (priority: %s)\n",
			at.Task.Name, at.Task.ID, at.PlanName, at.MilestoneName, at.Task.Priority)
	}
	sb.WriteString("\n")

	return nil
}

// writeOperationalMandates writes the standard CASAT operational mandates
// that all agents must follow.
func (g *SuperContextGenerator) writeOperationalMandates(sb *strings.Builder) {
	sb.WriteString("## CASAT Operational Mandates\n\n")
	sb.WriteString("All activities — planning, documentation, code discovery, and project management — MUST be performed using CASAT MCP tools.\n")
	sb.WriteString("Before writing any code, use `search_code` and `search_docs` to audit existing logic.\n")
	sb.WriteString("All project plans, milestones, and tasks MUST be managed via the CASAT API (`list_tasks`, `update_task_status`, `add_comment`).\n")
	sb.WriteString("Before submitting code, validate with configured build and test commands via CASAT pipelines.\n\n")
}

// writeToolWorkflow writes the tool-first workflow section with key tool
// invocation patterns for agent reference.
func (g *SuperContextGenerator) writeToolWorkflow(sb *strings.Builder) {
	sb.WriteString("## Tool-First Workflow\n\n")
	sb.WriteString("Use CASAT MCP tools for all workspace operations. Key workflows:\n\n")
	sb.WriteString("- **Coding Standards:** Run `get_standards` or `get_standard_section` for project conventions.\n")
	sb.WriteString("- **Project Structure:** Run `get_project_structure` to explore the directory tree, `list_files_in_dir` to explore a specific sub-directory.\n")
	sb.WriteString("- **Documentation:** Run `search_docs` to find docs by keyword, `list_docs` for all available docs, `get_doc_content` to read one.\n")
	sb.WriteString("- **Code Search:** Run `search_code` for symbols, `get_code_outline` for file structure, `get_dependencies` for import graphs.\n")
	sb.WriteString("- **File Operations:** Use `rm` to delete files, `mv` to move/rename, `mkdir` to create directories, `list_files_in_dir` to browse directory contents.\n")
	sb.WriteString("- **Refactoring:** Use `begin_refactor_transaction` before multi-file changes. Use `extract_code_block`, `insert_code_block`, `move_symbol` for code movement.\n")
	sb.WriteString("- **Build & Validation:** Run `discover_pipelines` to find pipelines, `run_pipeline` to execute.\n")
	sb.WriteString("- **Logs & Debugging:** Run `list_logs` then `get_log_errors` to diagnose issues.\n")
	sb.WriteString("- **Project Management:** Run `list_tasks` for current tasks, `list_projects` for project plans.\n\n")
}

// writeCodingStandards adds a section directing agents to retrieve coding
// standards on demand rather than embedding the full text, which keeps the
// context file concise.
func (g *SuperContextGenerator) writeCodingStandards(sb *strings.Builder, rootPath string) {
	sb.WriteString("## Coding Standards\n\n")

	guidelinesPath := filepath.Join(rootPath, "docs", "CodingGuidelines.md")
	if _, err := os.Stat(guidelinesPath); err == nil {
		sb.WriteString("Run `get_standards` or `get_standard_section` to retrieve project-specific coding conventions on demand.\n\n")
	} else {
		sb.WriteString("No coding standards file found. Create `docs/CodingGuidelines.md` to define project conventions.\n\n")
	}
}

// writeRecentDocs lists markdown files found in the workspace's docs/
// directory so agents are aware of available documentation.
func (g *SuperContextGenerator) writeRecentDocs(sb *strings.Builder, rootPath string) {
	docsDir := filepath.Join(rootPath, "docs")
	entries, err := os.ReadDir(docsDir)
	if err != nil {
		// No docs directory is not an error; just skip the section.
		return
	}

	var docFiles []string
	for _, e := range entries {
		if e.IsDir() || !strings.HasSuffix(strings.ToLower(e.Name()), ".md") {
			continue
		}
		docFiles = append(docFiles, e.Name())
	}

	if len(docFiles) == 0 {
		return
	}

	sb.WriteString("## Documentation\n\n")
	sb.WriteString("Available documentation in `docs/`:\n\n")
	for _, name := range docFiles {
		fmt.Fprintf(sb, "- `%s`\n", name)
	}
	sb.WriteString("\n")
}

// writePipelines queries the Pipelines repo for definitions in the workspace
// and lists them so agents know what build/test/validation pipelines exist.
func (g *SuperContextGenerator) writePipelines(ctx context.Context, sb *strings.Builder, workspaceID string) error {
	if g.store.Pipelines == nil {
		return nil
	}

	pipelines, err := g.store.Pipelines.ListPipelines(ctx, workspaceID)
	if err != nil {
		return fmt.Errorf("context.SuperContextGenerator.writePipelines: %w", err)
	}

	if len(pipelines) == 0 {
		return nil
	}

	sb.WriteString("## Pipelines\n\n")
	sb.WriteString("Available build and validation pipelines:\n\n")
	for _, p := range pipelines {
		desc := p.Description
		if desc == "" {
			desc = "No description"
		}
		fmt.Fprintf(sb, "- **%s** (`%s`) — %s\n", p.Name, p.ID, desc)
	}
	sb.WriteString("\n")

	return nil
}

// ---------------------------------------------------------------------------
// Internal types
// ---------------------------------------------------------------------------

// taskWithContext pairs a Task with its parent plan and milestone names for
// display purposes.
type taskWithContext struct {
	Task          *repo.Task
	PlanName      string
	MilestoneName string
}

// GeneratedAt returns the current time formatted for inclusion in generated
// files. Exported for use in templates or extended generators.
func GeneratedAt() string {
	return time.Now().UTC().Format(time.RFC3339)
}
