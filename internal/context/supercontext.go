// Package context implements the Super Context generator for the Hyperax Agent OS.
//
// Super Context watches workspaces and auto-generates agent context files
// (CLAUDE.md, .gemini, etc.) by pulling data from project plans, active tasks,
// coding standards, documentation, and pipeline definitions. The generated
// files carry a CASAT_MANAGED header so agents know not to edit them manually.
package context

import (
	"context"
	"fmt"
	"log/slog"
	"os"
	"path/filepath"
	"strings"
	"time"

	"github.com/hyperax/hyperax/internal/nervous"
	"github.com/hyperax/hyperax/internal/repo"
	"github.com/hyperax/hyperax/internal/storage"
)

// managedHeader is prepended to every generated context file so agents and
// humans know the file is machine-managed and should not be edited by hand.
const managedHeader = "<!-- CASAT_MANAGED: Do not edit manually. Regenerated by CASAT SuperContext System. -->"

// SuperContextGenerator produces structured agent context files by querying
// the Store for workspace metadata, project plans, tasks, pipelines, and docs.
type SuperContextGenerator struct {
	store  *storage.Store
	bus    *nervous.EventBus
	logger *slog.Logger
}

// NewSuperContextGenerator creates a SuperContextGenerator.
//
// Parameters:
//   - store: the composed Store facade providing access to all repos.
//   - bus: the EventBus for publishing generation events (may be nil).
//   - logger: structured logger for operational diagnostics.
func NewSuperContextGenerator(store *storage.Store, bus *nervous.EventBus, logger *slog.Logger) *SuperContextGenerator {
	if logger == nil {
		logger = slog.Default()
	}
	return &SuperContextGenerator{
		store:  store,
		bus:    bus,
		logger: logger,
	}
}

// Generate produces the full context content for a workspace as a structured
// markdown document. It queries the store for projects, tasks, standards,
// docs, and pipelines, assembling them into a format useful for AI agents.
//
// Parameters:
//   - ctx: cancellation context.
//   - workspaceID: the workspace name/ID to generate context for.
//   - rootPath: the filesystem root path of the workspace.
//
// Returns the generated markdown string, or an error if the workspace cannot
// be resolved or a critical query fails.
func (g *SuperContextGenerator) Generate(ctx context.Context, workspaceID string, rootPath string) (string, error) {
	g.logger.Info("generating super context", "workspace", workspaceID, "root", rootPath)

	var sb strings.Builder

	// Header
	sb.WriteString(managedHeader)
	sb.WriteString("\n\n")

	// Project Overview
	g.writeProjectOverview(&sb, workspaceID, rootPath)

	// Active Project Plans
	if err := g.writeProjectPlans(ctx, &sb, workspaceID); err != nil {
		g.logger.Warn("failed to write project plans section", "error", err)
	}

	// Active Tasks (in-progress)
	if err := g.writeActiveTasks(ctx, &sb, workspaceID); err != nil {
		g.logger.Warn("failed to write active tasks section", "error", err)
	}

	// CASAT Operational Mandates
	g.writeOperationalMandates(&sb)

	// Tool-First Workflow
	g.writeToolWorkflow(&sb)

	// Coding Standards
	g.writeCodingStandards(&sb, rootPath)

	// Recent Documentation
	g.writeRecentDocs(&sb, rootPath)

	// Pipeline Definitions
	if err := g.writePipelines(ctx, &sb, workspaceID); err != nil {
		g.logger.Warn("failed to write pipelines section", "error", err)
	}

	return sb.String(), nil
}

// WriteContextFile generates and writes the context to a CLAUDE.md file in
// the workspace root. If the file already exists and carries the CASAT_MANAGED
// header, it is overwritten. Files without the header are left untouched to
// avoid clobbering user-authored content.
//
// Parameters:
//   - ctx: cancellation context.
//   - workspaceID: the workspace name/ID.
//   - rootPath: the filesystem root path of the workspace.
//
// Returns an error if generation or file I/O fails.
func (g *SuperContextGenerator) WriteContextFile(ctx context.Context, workspaceID string, rootPath string) error {
	content, err := g.Generate(ctx, workspaceID, rootPath)
	if err != nil {
		return fmt.Errorf("context.SuperContextGenerator.WriteContextFile: generate: %w", err)
	}

	targetPath := filepath.Join(rootPath, string(ContextClaude))

	// Safety check: if the file exists but is NOT managed by us, refuse to
	// overwrite. This prevents destroying user-authored CLAUDE.md files.
	if existing, readErr := os.ReadFile(targetPath); readErr == nil {
		if !strings.HasPrefix(string(existing), "<!-- CASAT_MANAGED") {
			return fmt.Errorf("context.SuperContextGenerator.WriteContextFile: refusing to overwrite %s: file exists but is not CASAT-managed", targetPath)
		}
	}

	if err := os.WriteFile(targetPath, []byte(content), 0o644); err != nil {
		return fmt.Errorf("context.SuperContextGenerator.WriteContextFile: write file: %w", err)
	}

	g.logger.Info("wrote super context file", "path", targetPath, "workspace", workspaceID)
	return nil
}

